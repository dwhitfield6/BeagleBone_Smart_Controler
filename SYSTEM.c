/* Init_Modules
 *
 * Initializes the AM335x modules.
 *                                                                            */
/******************************************************************************/
void Init_Modules(void)
{
    /* Initialze ARM interrupt controller */
    IntAINTCInit();

    /* Enabling IRQ in CPSR of ARM processor. */
    IntMasterIRQEnable();
	Init_GPIO();
	Init_SPI();
    Init_LCD();
    Init_GUI();
	Init_LED();
	InitCMD();
	Init_UART();
}

